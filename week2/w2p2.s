Reset_Handler
	LDR R0,=N1
	LDR R1,=N2
	LDR R2,[R0]
	LDR R3,[R1]
	MOV R4, #4
	LDR R5, =DST
	MOV R6, #0
	
UP	ADDS R2,R3
	MOV R6, #0
	ADC R6, #0
	STR R2, [R5], #4
	LDR R2, [R0, #4]!
	LDR R3, [R1, #4]!
	SUBS R4, #1
	BNE UP
	STR R6, [R5]
STOP
	B STOP
N1 DCD 0x00000001,0x00000001,0x00000001,0x00000001
N2 DCD 0x00000002,0x00000002,0x00000002,0x00000002
