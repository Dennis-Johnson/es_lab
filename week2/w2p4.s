		DCD 0x10001000
		DCD Reset_Handler
		ALIGN
		AREA mycode,CODE,READONLY
		ENTRY
		EXPORT Reset_Handler
Reset_Handler
	LDR R0,=N1
	LDR R1,=N2
	LDR R4,=DST
	MOV R5,#4
	LDR R6, =0xFFFFFFFF
	MSR XPSR ,R6
up	LDR R2, [R0], #4
	LDR R3, [R1], #4
	SBCS R2, R2, R3
	STR R2, [R4], #4  
	SUBS R5, #1
	BNE up
STOP
	B STOP
N1 DCD 0x00000005,0x00000005,0x00000005,0x00000005
N2 DCD 0x00000005,0x00000004,0x00000003,0x00000002
	AREA mydata, DATA, READWRITE
DST DCD 0
	END