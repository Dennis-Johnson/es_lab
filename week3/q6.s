		AREA RESET,DATA,READONLY
		EXPORT __Vectors
___Vectors
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0, =VALUE
	LDR R1, [R0]
	LDR R2, =0X0F000000
	LDR R3, =RESULT
	LDR R5, =12
	LDR R4, =0
UP  AND R6,R1,R2
	LSR R6,R5
	SUB R5,#4
	LSR R2,#8
	ADD R4,R6
	CMP R2,#0
	BNE UP
	STR R4,[R3]
STOP 
	B STOP
VALUE DCD 01,02,03,04
	AREA mydata,DATA,READWRITE
RESULT DCD 0
	END